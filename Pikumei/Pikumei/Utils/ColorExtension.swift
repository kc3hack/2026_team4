import SwiftUI

// カラーアセットから Xcode が自動生成するため、手動での定義は不要
// 利用例:
//   Color.fireType     — 濃い色（アイコン・テキスト用）
//   Color.fireTypeBg   — パステル（カード背景用）

extension Color {
    /// アプリ共通のネイビー (#1E3365)
    static let pikumeiNavy = Color(red: 0x1E / 255.0, green: 0x33 / 255.0, blue: 0x65 / 255.0)
}
